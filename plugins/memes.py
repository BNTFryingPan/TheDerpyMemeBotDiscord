## TDMB Builtin Plugin
## Memes
## This plugin will add commands to manage a list of memes
## There will be a list of default memes, and a list for each server.

## !memes add default <name> <meme...> (bot owner only)
## !memes add <name> <meme...> (admin)
## !memes del default <meme> (bot owner only)
## !memes del <name> (admin)
## !memes <name> (anyone)